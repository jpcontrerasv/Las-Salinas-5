            	<p>Durante el mes de Abril del año en curso, se presentó a la comunidad la propuesta denominada  “Plan Maestro integrado, Antofagasta Sostenible”, una iniciativa liderara por el Gobierno Regional conjuntamente con el Municipio de la ciudad. Se trata de una iniciativa que se venía ya elaborando desde hace unos meses atrás, y que se inserta dentro de los compromisos adquiridos por nuestro país al integrar la Organización para la Cooperación y el Desarrollo Económico, OCDE.</p>

<p>En dicha ocasión, se dio a conocer la Secretaria Ejecutiva liderada por el arquitecto Alejandro Gutiérrez, quién se encargará específicamente, junto con varios colaboradores más,  de la gestión y la ejecución del Plan Maestro Integrado para la ciudad.</p>

<p>Este trabajo está encabezado por un Comité Público Privado liderado por el Intendente y la Alcaldesa de la ciudad, que trabajarán con un grupo de asesores directos a un nivel estratégico, conformado en un principio por los Ministerios Públicos, las Universidades y las empresas privadas más importantes de la región. Luego hay un siguiente nivel denominado Comité Ejecutivo integrado también por un grupo de personas que apoyarán las decisiones a nivel técnico y por último, está el comité de expertos liderados por la Secretaria Ejecutiva del Plan.</p>

<h3>Los objetivos del Plan Maestro Integrado</h3>

<p>Dentro de los objetivos que se propone este trabajo es la elaboración de un plan integral para la ciudad de Antofagasta, apostando por un desarrollo sostenible incluyendo mejores servicios e infraestructuras urbanas, y hacer converger todas las iniciativas que están en desarrollo hoy en la ciudad hacia una única visión aprovechando la oportunidad que se presentará en la región en los próximos diez años debido principalmente al crecimiento de las inversiones en el área de la minería, lo cual generará sin duda una gran presión por demandas habitacionales, de servicios y áreas de esparcimiento, sobre las áreas urbanas y consolidadas y de expansión urbana.</p>

 

<h3>¿Por qué es tan necesario contar con un Plan Maestro para el desarrollo de la ciudad?</h3>

<p>El Municipio está liderando la idea de la implementación de un Tranvía para la ciudad cuyo estudio a nivel de pre factibilidad fue realizado por una consultora Francesa de mucha experiencia en este tipo de proyectos. Por otra parte, el Ministerio de Transporte y telecomunicaciones está elaborando un “Plan de Trasporte Urbano” para la ciudad y el Ministerio de Vivienda y Urbanismo está trabajando en un programa de Desarrollo urbano. Complementariamente existe un proyecto denominado “Región fértil”, que a través de la empresa trasnacional IBM ha desarrollado un diagnóstico del consumo de agua en las áreas verdes de la ciudad. Todas estas iniciativas públicas y otras privadas son una excelente oportunidad para mejorar la calidad de vida de la ciudad. Sin embargo, estas iniciativas requieren de una profunda articulación entorno a un Plan integral de ciudad.</p>

 

<h3>La primera gran renovación urbana de la ciudad</h3>

<p>Esta idea de implementar un Plan Maestro para la ciudad no es nueva, ya a finales de los años 90 cuando se realizó por primera vez la primera gran Renovación Urbana del centro de la ciudad y su área portuaria, se elaboró por parte del MOP el llamado “Plan Gubbins”, un estudio de diagnóstico y de propuestas vertidas en un Plan Maestro que elaboró una imagen objetivo en tres dimensiones mediante una maqueta y vistas perspectivadas  y que posteriormente dieron origen a los proyectos Bicentenarios que le cambiaron el rostro a una parte importante de la ciudad, logrando implementar la costanera central, costanera norte, los paseos peatonales, las playas artificiales y la construcción de un gran centro comercial con una plaza pública en la zona portuaria.</p>

<p>Es cierto que muchos de estos proyectos ya se encontraban en carpeta en distintas instituciones públicas, entonces la importancia del Plan fue la de hacer converger en un solo gran proyecto todas esas iniciativas, generando sinergias positivas, logrando que la ciudad de Antofagasta finalizara sus obras Bicentenarios con dos años de anticipación. Un tema pendiente y que se desprende de esa experiencia, fue la necesidad de incorporar el concepto de mantención del nuevo espacio público que se generó con la ejecución de los nuevos proyectos urbanos en la ciudad.</p>

<p>En este sentido, urge contar a la brevedad con un Plan Maestro Integral sobre todo de mejora del espacio público no solo del borde costero sino que de toda la ciudad, un Plan Maestro Urbano  que sea consensuado con la comunidad, estratégico que ponga énfasis en lo más urgente, flexible en el sentido de acomodarse a los escenarios cambiantes, y sostenible tanto en el sentido económico como ambiental que incorpore el reciclaje y las energías alternativas.</p>

<p>A la luz de las excelentes proyecciones de crecimiento para la región y por ende para la ciudad, es tiempo de generar ya la segunda Gran Renovación Urbana.</p>

