                <p>La condición impuesta por Levisky Arquitetos Asociados, la firma paulista contratada para la implementación de un nuevo proyecto, fue que este mismo se incluyera como uno de los términos del acuerdo legal entre las partes. De esta manera se garantizó la concreción del diseño original con todos sus componentes de arquitectura, paisaje e ingeniería.</p>

<p>El sitio era un importante incinerador de la ciudad que luego de cuarenta años de continuo funcionamiento fue desactivado en 1990 y propuesto para su total recuperación y reconversión en el 2006. Luego de varios estudios químicos realizados sobre el estado del suelo, los cuales dejaron como resultado el alto nivel de contaminación del mismo, quedó expuesta la necesidad de un diseño innovador que permitiera el acceso del público sin recurrir a la tradicional acción de recubrir la superficie existente con nuevo suelo y nueva vegetación.</p>

<p>El estudio de arquitectos incorporó al equipo a Anna Dietzsch, arquitecta brasileña que pertenece desde 1993 al renombrado estudio americano Davis Brady Bond Aeda (DBBA Architects). Su propuesta de diseño se basa en la utilización de un deck de madera continuo y elevado aproximadamente unos 90 cm que, por medio de unas estructuras metálicas recicladas, busca separarse del suelo contaminado.</p>

<p>Atravesando el sitio con una diagonal que permite el desarrollo del recorrido linear más largo, esta plataforma se define como una pieza única que comienza en la rampa de acceso planteándose ya como eje central visual y físico y luego moldeando plataformas de observación, mobiliario urbano y bordes envolventes. Los paneles horizontales se transforman en paneles verticales que hacen las veces de cerramiento y borde, dejando a los lados las áreas exhibidas temáticamente, tanto con plantaciones nuevas como las preservadas con árboles existentes. En estas últimas es donde se agregó una nueva capa de tierra.</p>

<p>En el centro del sitio, el diseño de un anfiteatro abierto refuerza la continuidad de líneas arquitectónicas y la fluidez tridimensional que definen al proyecto, sin por eso perder un diálogo constante con las formas de la naturaleza.</p>

<p>Reciclado como Museo de Sustentabilidad, el antiguo edificio de ladrillo en donde funcionaba el incinerador expone la historia del sitio, completando la experiencia propuesta a los visitantes de enfrentar viejos y nuevos usos, pasado y presente, abandono y reconversión.</p>
